"""Задание 11 (1 балл): Описать функцию, которая принимает на вход два списка и возвращает
 список уникальных элементов, которые есть в первом входном списке и отсутствуют во втором.
 Запрещается использовать циклы и списковые включения/генераторы спискoв."""


def task_11_func(first_list: list, second_list: list) -> list:
    """

            функция принимаетна вход два списка и возвращает
            список уникальных элементов, которые есть в первом
            входном списке и отсутствуют во втором.
            """
    result = set(first_list) - set(second_list)

    return list(result)


